<?php

function columnas($column)
{
    switch ($column) {
        case 'id':
            return "id=";
            break;
        case 'nombre':
            return "nombre=";
            break;
        case 'login':
            return "login=";
            break;
        case 'apellidos':
            return "apellidos=";
            break;
        case 'password':
            return "id=";
            break;
        case 'email':
            return "id=";
            break;
        case 'tipo':
            return "id=";
            break;
        case 'fecha_nacimiento':
            return "id=";
            break;
        case 'num_tlf':
            return "id=";
            break;
        case 'rol':
            return "id=";
            break;
        case 'fecha_alta':
            return "id=";
            break;
        case 'estado':
            return "id=";
            break;
        case 'token':
            return "id=";
            break;
        case 'fecha_token':
            return "id=";
            break;
        default:
            return "id=";
            break;
    }
}
